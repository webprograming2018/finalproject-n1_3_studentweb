package model;

/**
 *
 * @author Admin
 */
public class News {
    private String title, url, image, category;

    public News() {
    }

    public News(String title, String url, String image, String category) {
        this.title = title;
        this.url = url;
        this.image = image;
        this.category = category;
    }
    
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }
    
}
